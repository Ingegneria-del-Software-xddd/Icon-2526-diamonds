% Classificazione per CARAT
carat_class(Carat, low) :- Carat < 0.3.
carat_class(Carat, medium) :- Carat >= 0.3, Carat < 0.7.
carat_class(Carat, high) :- Carat >= 0.7.

% Classificazione per DEPTH  
depth_class(Depth, low) :- Depth < 60.
depth_class(Depth, medium) :- Depth >= 60, Depth =< 64.
depth_class(Depth, high) :- Depth > 64.

% Classificazione per TABLE
table_class(Table, low) :- Table < 55.
table_class(Table, medium) :- Table >= 55, Table < 65.
table_class(Table, high) :- Table >= 65.

% Classificazione per X
x_class(X, low) :- X < 4.0.
x_class(X, medium) :- X >= 4.0, X < 6.0.
x_class(X, high) :- X >= 6.0.

% Classificazione per Y
y_class(Y, low) :- Y < 4.0.
y_class(Y, medium) :- Y >= 4.0, Y < 6.0.
y_class(Y, high) :- Y >= 6.0.

% Classificazione per Z
z_class(Z, low) :- Z < 2.5.
z_class(Z, medium) :- Z >= 2.5, Z < 4.0.
z_class(Z, high) :- Z >= 4.0.

% Classificazione per PRICE
price_class(Price, low) :- Price < 500.
price_class(Price, medium) :- Price >= 500, Price < 2000.
price_class(Price, high) :- Price >= 2000.



% Regole per ottenere le classificazioni dei diamanti
prop(Diamond, carat_class, Class) :-
    prop(Diamond, carat, Carat),
    carat_class(Carat, Class).

prop(Diamond, depth_class, Class) :-
    prop(Diamond, depth, Depth),
    depth_class(Depth, Class).

prop(Diamond, table_class, Class) :-
    prop(Diamond, table, Table),
    table_class(Table, Class).

prop(Diamond, x_class, Class) :-
    prop(Diamond, x, X),
    x_class(X, Class).

prop(Diamond, y_class, Class) :-
    prop(Diamond, y, Y),
    y_class(Y, Class).

prop(Diamond, z_class, Class) :-
    prop(Diamond, z, Z),
    z_class(Z, Class).

prop(Diamond, price_class, Class) :-
    prop(Diamond, price, Price),
    price_class(Price, Class).








